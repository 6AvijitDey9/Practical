package String;
public class SubString {

	public static void main(String[] args) {
		String name="Avijit Dey";
		System.out.println("Substring is with begin index 2: " + name.substring(2));
		System.out.println("Substring is with begin index 0 & End index 9: " + name.substring(0, 9));
	}
}
