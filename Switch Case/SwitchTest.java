package Default;

public class SwitchTest {

	public static void main(String[] args) {
		int num=10;//declaring a variable for switch case
		//switch expression
		switch(num) {
		//case expression
		case 1: System.out.println("12");
		break;
		case 2: System.out.println("10");
		break;
		case 12: System.out.println("13");
		break;
		case 10: System.out.println("15");
		break;
		//default case statement
		default: System.out.println("Number is not valid");
		}

	}

}
